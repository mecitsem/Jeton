        public static string EncodeBase64(string text)
        {
            try
            {
                var encDataByte = new byte[text.Length];
                encDataByte = Encoding.UTF8.GetBytes(text);
                string encodedData = Convert.ToBase64String(encDataByte);
                return encodedData;
            }
            catch (Exception ex)
            {
                throw new Exception("ErrorEncodeBase64" + ex.Message);
            }
        } //this function Convert to Decord your Password
        public static string DecodeBase64(string encodedData)
        {
            var encoder = new UTF8Encoding();
            var utf8Decode = encoder.GetDecoder();
            var todecodeByte = Convert.FromBase64String(encodedData);
            var charCount = utf8Decode.GetCharCount(todecodeByte, 0, todecodeByte.Length);
            var decodedChar = new char[charCount];
            utf8Decode.GetChars(todecodeByte, 0, todecodeByte.Length, decodedChar, 0);
            var result = new String(decodedChar);
            return result;
        }
